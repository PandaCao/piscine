#include <unistd.h>

int	main(int argc, char *argv[])
{
	int	i;
	int	a;
	int	tmp;

	i = 0;
	a = 0;
	tmp = argc -1;
	if (argc > 1)
	{
		while (0 < tmp)
		{
			a = 0;
			while (argv[tmp][a] != '\0')
			{
				write(1, &argv[tmp][a], 1);
				a++;
			}
			write(1, "\n", 1);
			tmp--;
		}
	}
	return (0);
}
